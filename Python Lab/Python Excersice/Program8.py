# Write a python program to add new data item into list using append() and insert().

# Define an empty list
my_list = [10,20,30,40]
print("Original list", my_list)

# Using append() to add an item at the end
my_list.append(50)
print("List after append(50):", my_list)

# Using insert() to add an item at a specific index (here index 2)
my_list.insert(2,23)
print("List after insert(2,23)", my_list)